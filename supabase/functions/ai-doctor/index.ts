import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { treatmentType, disease, method } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get('LOVABLE_API_KEY');
    
    if (!LOVABLE_API_KEY) {
      throw new Error('LOVABLE_API_KEY is not configured');
    }

    const prompt = `You are an expert agricultural consultant. Provide a detailed, step-by-step treatment plan for the following:

Treatment Type: ${treatmentType}
Disease/Condition: ${disease}
Treatment Method: ${method}

Provide a comprehensive, numbered step-by-step guide that includes:
1. Preparation steps
2. Application instructions
3. Safety precautions
4. Expected timeline
5. Follow-up care
6. Monitoring tips

Format the response in clear, actionable steps that a farmer can easily follow. Use simple language and include specific measurements, timings, and precautions.`;

    const response = await fetch('https://ai.gateway.lovable.dev/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${LOVABLE_API_KEY}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'google/gemini-2.5-flash',
        messages: [
          { role: 'system', content: 'You are an expert agricultural consultant providing detailed farming treatment plans. Always respond with clear, numbered steps.' },
          { role: 'user', content: prompt }
        ],
        temperature: 0.7,
        max_tokens: 2000,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      throw new Error(`API error: ${response.status} - ${errorText}`);
    }

    const data = await response.json();
    const detailedSteps = data.choices?.[0]?.message?.content || 'Unable to generate detailed steps.';

    return new Response(
      JSON.stringify({ detailedSteps }),
      {
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
      }
    );
  } catch (error: any) {
    return new Response(
      JSON.stringify({ error: error.message }),
      {
        status: 500,
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
      }
    );
  }
});

